import java.util.*;

class CharDemo {
    public static void main(String[] args) {
        char c1, c2;
        c1 = 88;
        c2 = 'Y';

        System.out.println("c1 and c2 : " + c1 + " " + c2);
        System.out.println("c2 after increment: " + ++c2);

        System.out.println("10 is greater than 9 : " + (10 > 9));
    }
}